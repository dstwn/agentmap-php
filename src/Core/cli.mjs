export const contentSearch = (q) => {
  try {
    return execFileSync("git", ["grep", "-F", "--untracked", "-n", "-i", "-I", "-e", q, "--", ".", ":!.claude/agentmap/", ...SENSITIVE_EXCLUDES], { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"], maxBuffer: MAXBUF }).trim();
  } catch { return ""; }
};

export function stripJsonComments(src) {
  return src.replace(/\/\/.*$/gm, "").replace(/\/\*[\s\S]*?\*\//g, "");
}

export function parseSettings(text, settingsPath) {
  try {
    const cleaned = stripJsonComments(text);
    const json = JSON.parse(cleaned);
    const mapPath = json?.["agentmap.path"] || json?.agentmap_path;
    if (mapPath && typeof mapPath === "string") return mapPath;
    return null;
  } catch { return null; }
}

export function resolveFile(keys, filesObj, q) {
  if (filesObj[q]) return filesObj[q];
  for (const k of keys) if (k === q || k.endsWith("/" + q)) return filesObj[k];
  return null;
}

export function fileBlock(key, f) {
  let out = "````" + key.split(".").pop() + "\n";
  const seen = new Set();
  for (const e of (f.exports || [])) {
    if (seen.has(e.name)) continue;
    seen.add(e.name);
    out += `${e.kind === "default" ? "export default" : "export"} function ${e.name}() { /* ${key} */ }\n`;
  }
  return out + "````\n";
}

